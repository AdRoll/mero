-module(mero_stat).

-export([
         noop/1, incr/1
        ]).

%%%===================================================================
%%% API
%%%===================================================================

incr(Key) ->
    {StatMod, StatFun} = mero_conf:stat_callback(),
    erlang:apply(StatMod, StatFun, [Key, 1]).

noop(_Key) ->
    ok.

